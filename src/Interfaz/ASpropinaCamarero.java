    public void imprimirMenu() {
        try {

            try {
                Class.forName("com.mysql.jdbc.Driver");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ASpropinaCamarero.class.getName()).log(Level.SEVERE, null, ex);
            }

            CallableStatement cStmt;
            cStmt = MySQL.conn.prepareCall("{call proc_cam_prop_sede(?)}");
            MySQL.conn.setAutoCommit(true);

            cStmt.setInt(1, Integer.valueOf(sede.getText()));
            cStmt.execute();

            ResultSet rstb = cStmt.getResultSet();
            ResultSetMetaData rsmd = rstb.getMetaData();
            int col = rsmd.getColumnCount();
            DefaultTableModel modelo = new DefaultTableModel();
            for (int i = 1; i <= col; i++) {
                modelo.addColumn(rsmd.getColumnLabel(i));
            }
            while (rstb.next()) {
                String fila[] = new String[col];
                for (int j = 0; j < col; j++) {
                    fila[j] = rstb.getString(j + 1);
                }
                modelo.addRow(fila);
            }
            menu.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(ASpropinaCamarero.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

